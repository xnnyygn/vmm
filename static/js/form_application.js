$(function(){
    $('input[name="hostname_autogenerated"]').click(function(){
        var checked = $(this).filter(':checked').length != 0;
        var hostnameInput = $('input[name="hostname"]');
        if(checked) {
            hostnameInput.attr('disabled', 'disabled');
        } else {
            hostnameInput.removeAttr('disabled');
        }
    }).click();
});
